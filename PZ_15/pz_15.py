# Вариант 1
import sqlite3

# Создаем соединение с базой данных
conn = sqlite3.connect('optovaya_base.db')

# Создаем таблицу Товары
conn.execute('''
    CREATE TABLE IF NOT EXISTS Товары (
        Код_товара INTEGER PRIMARY KEY,
        Наименование_товара TEXT,
        Наименование_магазина TEXT,
        Заявки_магазина INTEGER,
        Количество_на_складе INTEGER,
        Единицы_измерения TEXT,
        Оптовая_цена REAL
    );
''')

# Добавляем товары в таблицу
conn.execute('''
    INSERT INTO Товары (Код_товара, Наименование_товара, Наименование_магазина, Заявки_магазина, Количество_на_складе, Единицы_измерения, Оптовая_цена)
    VALUES (1, 'Товар 1', 'Магазин 1', 10, 100, 'шт', 100.0);
''')

conn.execute('''
    INSERT INTO Товары (Код_товара, Наименование_товара, Наименование_магазина, Заявки_магазина, Количество_на_складе, Единицы_измерения, Оптовая_цена)
    VALUES (2, 'Товар 2', 'Магазин 2', 20, 200, 'шт', 200.0);
''')

# Выводим все товары из таблицы
for row in conn.execute('SELECT * FROM Товары'):
    print(row)

# Закрываем соединение с базой данных
conn.close()
